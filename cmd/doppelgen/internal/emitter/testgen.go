// sliceTestValue returns a Go literal for a non-empty slice.
func sliceTestValue(field types.FieldInfo) string {
	elem := field.ElemType
	if isBuiltinPrimitive(elem) {
		return fmt.Sprintf("%s{%s, %s}", field.Type, primitiveTestValue(elem), primitiveTestValueAlternate(elem))
	}
	// Struct element — use zero-value structs.
	return fmt.Sprintf("%s{{}, {}}", field.Type)
}

// mapTestLiteral returns a Go literal for a non-empty map.
func mapTestLiteral(field types.FieldInfo) string {
	key := field.KeyType
	val := field.ValueType
	return fmt.Sprintf("%s{%s: %s}", field.Type, mapTestKey(key), mapTestValueLiteral(val))
}

// mapTestKey returns a Go expression for a test map key.
func mapTestKey(keyType string) string {
	if isBuiltinPrimitive(keyType) {
		v := primitiveTestValue(keyType)
		// For string keys, use a shorter value.
		if keyType == "string" {
			return `"k"`
		}
		return v
	}
	return fmt.Sprintf("%s{}", keyType)
}

// mapTestValueLiteral returns a Go expression for a test map value.
func mapTestValueLiteral(valType string) string {
	if isBuiltinPrimitive(valType) {
		return primitiveTestValue(valType)
	}
	return fmt.Sprintf("%s{}", valType)
}

