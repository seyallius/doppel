// -------------------------------------------- Field Category Predicates --------------------------------------------

// hasPtrPrimitiveFields reports whether the struct has any pointer-to-primitive fields.
func (e *testEmitter) hasPtrPrimitiveFields(info *types.StructInfo) bool {
	for _, field := range info.Fields {
		if field.TypeCategory == types.CatPtrPrimitive {
			return true
		}
	}
	return false
}

// hasDeepContainerFields reports whether the struct has any deep-copied container fields
// (slices, maps, pointers) where independence should be verified.
func (e *testEmitter) hasDeepContainerFields(info *types.StructInfo) bool {
	for _, field := range info.Fields {
		if isDeepCopiedContainer(field) {
			return true
		}
	}
	return false
}

// hasSkipFields reports whether the struct has any skip-tagged fields.
func (e *testEmitter) hasSkipFields(info *types.StructInfo) bool {
	for _, field := range info.Fields {
		if field.Directive.Skip {
			return true
		}
	}
	return false
}

// hasShallowContainerFields reports whether the struct has any shallow-tagged container fields.
func (e *testEmitter) hasShallowContainerFields(info *types.StructInfo) bool {
	for _, field := range info.Fields {
		if field.Directive.Shallow && isContainerType(field.TypeCategory) {
			return true
		}
	}
	return false
}

// hasEmptyFields reports whether the struct has any empty-tagged non-primitive fields.
func (e *testEmitter) hasEmptyFields(info *types.StructInfo) bool {
	for _, field := range info.Fields {
		if field.Directive.Empty && !isPrimitiveCategory(field.TypeCategory) {
			return true
		}
	}
	return false
}

// hasCloneTagFields reports whether the struct has any clone-tagged fields.
func (e *testEmitter) hasCloneTagFields(info *types.StructInfo) bool {
	for _, field := range info.Fields {
		if field.Directive.Clone {
			return true
		}
	}
	return false
}

// isDeepCopiedContainer reports whether a field is a deep-copied container
// (slice, map, or pointer) where the clone should be independent of the original.
func isDeepCopiedContainer(field types.FieldInfo) bool {
	if field.Directive.Skip || field.Directive.Shallow || field.Directive.Empty || field.Directive.Clone {
		return false
	}
	switch field.TypeCategory {
	case types.CatSlice, types.CatMap, types.CatPtrPrimitive, types.CatPtrStruct, types.CatStruct:
		return true
	}
	return false
}

// isContainerType reports whether the type category is a container (slice, map, pointer).
func isContainerType(cat types.TypeCategory) bool {
	switch cat {
	case types.CatSlice, types.CatMap, types.CatPtrPrimitive, types.CatPtrStruct:
		return true
	}
	return false
}

// -------------------------------------------- Output Helpers --------------------------------------------

func (e *testEmitter) emitRaw(s string) {
	e.buf.WriteString(strings.Repeat("\t", e.indent))
	e.buf.WriteString(s)
	e.buf.WriteString("\n")
}

func (e *testEmitter) emitBlankLine() {
	e.buf.WriteString("\n")
}

func (e *testEmitter) emitComment(s string) {
	e.emitRaw("// " + s)
}
